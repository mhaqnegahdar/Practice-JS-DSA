// Return the percentage of a number

// Write a function that takes a number and a percentage
// Return the percentage value of the number

function myFunction(num, percentage) {
    return (num *percentage)/100
  
}

console.log(myFunction(100, 50)); // 50
console.log(myFunction(200, 10)); // 20
console.log(myFunction(80, 25)); // 20